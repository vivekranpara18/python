#create function to convert meter into inches
def meter_to_inch(meter):
    inch = meter * 39.37
    return inch

#create function to convert foot into inches
def foot_to_inch(foot):
    inch = foot * 12
    return inch

#create function to convert cm into inches
def cm_to_inch(cm):
    inch = cm / 2.54
    return inch

